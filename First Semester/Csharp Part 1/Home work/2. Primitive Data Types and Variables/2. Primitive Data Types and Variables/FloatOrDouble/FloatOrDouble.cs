using System;

class FloatOrDouble
{
    static void Main()
    {
        float firstNumber = 12.345f;
        float secondNumber = 3456.091f;
        double thirdNumber = 8923.1234857d;
        double fourthNumber = 34.567839023d;
        Console.WriteLine("{0}, {1}, {2}, {3}", firstNumber, secondNumber, thirdNumber, fourthNumber);
    }
}

