using System;

class MostAppropriateVariable
{
    static void Main()
    {
        byte firstNumber = 97;
        sbyte secondNumber = -115;
        short thirdNumber = -10000;
        ushort fourthNumber = 52130;
        int fifthNumber = 4825932;
        Console.WriteLine("{0}, {1}, {2}, {3}, {4}", firstNumber, secondNumber, thirdNumber, fourthNumber, fifthNumber);
    }
}

